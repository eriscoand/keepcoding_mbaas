    func showMessage(userInfo: [AnyHashable : Any]){
        let notification = userInfo as NSDictionary as! [String: AnyObject]
        let messageInfo = notification["notification"] as! NSDictionary as! [String: AnyObject]
        let messageDict = messageInfo as NSDictionary
        
        if let message: String = messageDict.allValues.last as? String {
            let alert = UIAlertController(title: "Notification", message: message, preferredStyle: UIAlertControllerStyle.alert)
            alert.addAction(UIAlertAction(title: "OK", style: UIAlertActionStyle.default, handler: nil))
            self.window?.rootViewController?.present(alert, animated: true, completion: nil)
        }
        
    }
    
}
